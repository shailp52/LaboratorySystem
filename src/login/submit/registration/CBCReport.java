package login.submit.registration;

public class CBCReport {
	
	private int ReportID;
	private int CustID;
	private String Name;
	private String Gender;
	private java.util.Date date;
	private float Hb;
	private float RBC;
	private float WBC;
	private float Platelet;
	private float HCT;
	private float MCV;
	private float MCH;
	private float MCHC;
	
	public int getReportID() {
		return ReportID;
	}
	public void setReportID(int reportID) {
		ReportID = reportID;
	}
	public int getCustID() {
		return CustID;
	}
	public void setCustID(int custID) {
		CustID = custID;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getGender() {
		return Gender;
	}
	public void setGender(String gender) {
		Gender = gender;
	}
	public java.util.Date getDate() {
		return date;
	}
	public void setDate(java.util.Date date) {
		this.date = date;
	}
	public float getHb() {
		return Hb;
	}
	public void setHb(float hb) {
		Hb = hb;
	}
	public float getRBC() {
		return RBC;
	}
	public void setRBC(float rBC) {
		RBC = rBC;
	}
	public float getWBC() {
		return WBC;
	}
	public void setWBC(float wBC) {
		WBC = wBC;
	}
	public float getPlatelet() {
		return Platelet;
	}
	public void setPlatelet(float platelet) {
		Platelet = platelet;
	}
	public float getHCT() {
		return HCT;
	}
	public void setHCT(float hCT) {
		HCT = hCT;
	}
	public float getMCV() {
		return MCV;
	}
	public void setMCV(float mCV) {
		MCV = mCV;
	}
	public float getMCH() {
		return MCH;
	}
	public void setMCH(float mCH) {
		MCH = mCH;
	}
	public float getMCHC() {
		return MCHC;
	}
	public void setMCHC(float mCHC) {
		MCHC = mCHC;
	}
	
	
	

}
